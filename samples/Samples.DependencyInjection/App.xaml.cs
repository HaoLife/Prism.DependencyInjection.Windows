namespace Samples.DependencyInjection
{
    /// <summary>
    /// 提供特定于应用程序的行为，以补充默认的应用程序类。
    /// </summary>
    sealed partial class App : PrismDIApplication
    {
        /// <summary>
        /// 初始化单一实例应用程序对象。这是执行的创作代码的第一行，
        /// 已执行，逻辑上等同于 main() 或 WinMain()。
        /// </summary>
        public App()
        {
            this.InitializeComponent();
        }


        protected override Task OnLaunchApplicationAsync(LaunchActivatedEventArgs args)
        {
            NavigationService.Navigate("Main", null);
            return Task.FromResult<object>(null);
        }

        protected override void Configure(IServiceProvider provider)
        {
            base.Configure(provider);

            //todo:通过获取对象可以设置具体参数
            //provider.GetRequiredService<Prism.Windows.AppModel.IDeviceGestureService>().UseTitleBarBackButton = false;
